    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Instanciar objetos requeridos
        CRUDsql crud = new CRUDsql();
        Variables variables = new Variables();

        // Usar el método de mostrar
        crud.mostrar(txtIdEmpleado_Search.getText());
        
        // Colocar en los campos de texto los valores almacenados
        txtIdEmpleado.setText(variables.getId_empleado());
        txtNombre.setText(variables.getNombre());
        txtApellido.setText(variables.getApellido());
        txtPuesto.setText(variables.getPuesto());
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // Instanciar objetos requeridos
        CRUDsql crud = new CRUDsql();
        Variables variables = new Variables();
        
        // Almacenar los valores recolectados
        variables.setId_empleado(txtIdEmpleado.getText());
        variables.setNombre(txtNombre.getText());
        variables.setApellido(txtApellido.getText());
        variables.setPuesto(txtPuesto.getText());
        
        // Usar el método de actualizar
        crud.actualizar(
                variables.getNombre(), 
                variables.getApellido(), 
                variables.getPuesto(), 
                variables.getId_empleado()
        );
        
        //Limpiar los campos
        txtIdEmpleado.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtPuesto.setText("");
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Instanciar objetos requeridos
        CRUDsql crud = new CRUDsql();
        // Usar el método de eliminar
        crud.eliminar(txtIdEmpleado.getText());
        
        //Limpiar los campos
        txtIdEmpleado.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtPuesto.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

